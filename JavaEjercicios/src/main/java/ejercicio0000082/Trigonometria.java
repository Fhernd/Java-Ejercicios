package ejercicio0000082;

/**
 * Ejercicio 82: Uso de funciones trionométricas de la clase Math.
 * 
 * @author John Ortiz Ordoñez
 */
public class Trigonometria {
    public static void main(String[] args) {
        System.out.println(Math.sin(Math.PI / 2));
        System.out.println(Math.cos(Math.PI / 2));
        System.out.println(Math.tan(Math.PI / 2));
        System.out.println(Math.asin(Math.PI / 2));
        System.out.println(Math.acos(Math.PI / 2));
        System.out.println(Math.atan(Math.PI / 2));
        
        System.out.println(Math.sinh(Math.PI / 2));
        System.out.println(Math.cosh(Math.PI / 2));
        System.out.println(Math.tanh(Math.PI / 2));
        
    }
}
