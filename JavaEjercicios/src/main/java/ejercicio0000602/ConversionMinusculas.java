package ejercicio0000602;

/**
 * Ejercicio 602: Convertir un texto en mayúsculas en minúsculas con toLowerCase().
 *
 * @author John Ortiz Ordoñez.
 */
public class ConversionMinusculas {

    public static void main(String[] args) {
        String frase = "JAVA ES UN LENGUAJE DE PROGRAMACIÓN";
        System.out.println(frase);
        
        System.out.println();
        
        String resultado = frase.toLowerCase();
        System.out.println(resultado);
    }
}
