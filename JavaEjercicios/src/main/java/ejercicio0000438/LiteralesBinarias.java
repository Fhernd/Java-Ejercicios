public class LiteralesBinarias {

    public static void main(String[] args) {
        
        int doce = 0b1100;
        System.out.println(doce);   // 12
        
        System.out.println();
        
        int quince = 0B1111;
        System.out.println(quince); // 15
        
        System.out.println();
        
        int binario = 0b11100110011;
        System.out.println(binario);
    }
}
