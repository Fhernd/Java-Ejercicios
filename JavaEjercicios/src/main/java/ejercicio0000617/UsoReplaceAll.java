package ejercicio0000617;

/**
 * Ejercicio 617: Usar la función replaceAll() para reemplazar todas las coincidencias de un carácter por otro.
 *
 * @author: John Ortiz Ordoñez
 */
public class UsoReplaceAll {
    public static void main(String[] args) {
        System.out.println("COlOmbia".replaceAll("O", "o")); // Colombia
        System.out.println("C0l0mbia".replaceAll("0", "o")); // Colombia
    }
}
